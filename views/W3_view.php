				<h2><a href="http://www.gry-online.pl/S016.asp?ID=20436" target="_blank">Wiedźmin 3: Dziki Gon</a></h2>
				
				<p>Wiedźmin 3: Dziki Gon (ang. The Witcher 3: Wild Hunt) – fabularna gra akcji stworzona i wydana przez polskie studio CD Projekt RED 19 maja 2015 na platformy Microsoft Windows, PlayStation 4 i Xbox One.</p>

				<p>Gra jest kontynuacją wydanego w 2007 roku Wiedźmina oraz Wiedźmina 2: Zabójców królów, który miał swoją premierę w 2011 roku. Tak jak poprzednie części, opowiada ona historię tytułowego wiedźmina – Geralta z Rivii – i jest osadzona w świecie wiedźmina, którego twórcą jest Andrzej Sapkowski. Produkcja jest ostatnią częścią cyklu, którego głównym bohaterem jest tytułowy wiedźmin, razem z nią stanowiącego trylogię.</p>

				<p> Pierwszy zwiastun produkcji został zaprezentowany na konferencji Microsoftu na targach E3 w czerwcu 2013 roku. Ponadto Wiedźmin 3 zdobył tam kilkadziesiąt nagród oraz nominacji przyznanych przez czasopisma i portale z branży gier komputerowych.</p>
				<br />
				<p>Akcja Wiedźmina 3 rozgrywa się w fikcyjnym świecie fantasy stworzonym przez pisarza Andrzeja Sapkowskiego w serii opowiadań i powieści. Gracz ma okazję zwiedzić wyspy Skellige, Wolne Miasto Novigrad, Oxenfurt, wiedźmińskie siedliszcze Kaer Morhen czy ponure moczary Velen. Gra rozpoczyna się pół roku po wydarzeniach z poprzedniej części. Wiedźmin Geralt odzyskuje w końcu pamięć, nie ma jednak zamiaru przyłączyć się do którejkolwiek ze stron konfliktu. Zamiast tego zajmuje się on poszukiwaniem swoich bliskich, m.in. Yennefer z Vengerbergu, Ciri oraz tytułowego Dzikiego Gonu.</p>
	

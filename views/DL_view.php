				<h2><a href="http://www.gry-online.pl/S016.asp?ID=21264" target="_blank">Dying Light</a></h2>
				
				<p>Dying Light to gra akcji z otwartym światem, utrzymana w klimatach survival horroru. Tytuł opracowało polskie studio Techland. Jest to swoisty miks Dead Island, produkcji tego samego zespołu, oraz Mirror’s Edge od twórców serii Battlefield. Akcja gry została ukazana z perspektywy pierwszej osoby.</p>
				
				<p>Wcielamy się w niejakiego Kyle’a Crane’a, trafiając do miasta Harran, wzorowanego na Rio de Janeiro, pełnym charakterystycznych faweli. Ludność metropolii została zainfekowana tajemniczym wirusem, który błyskawicznie zmienił wszystkich w zombie. Twórcy gry wykorzystali tutaj motyw zbliżony do tego znanego z filmu Jestem Legendą z Willem Smithem w roli głównej – w ciągu dnia zombie są mało aktywne, ale w nocy uaktywniają się ich groźniejsze mutacje. W związku z tym rozgrywka dzieli się na dwie części - gdy jest jasno, swobodnie eksplorujemy otoczenie, poszukujemy surowców i broni, a gdy nastaje noc, desperacko bronimy się przed agresywną hordą.</p>
				
				<p>W Dying Light walczymy ze zmutowanymi ludźmi przede wszystkim za pomocą broni białej i obuchowej, rzadziej – palnej. Tytuł oferuje zróżnicowane uzbrojenie, od siekier i maczet, przez halabardy, a na młotach i kijach kończąc. Duży nacisk położono także na rozbudowany system tworzenia wyposażenia, dzięki czemu możemy połączyć pozornie nieprzydatne rzeczy z wybranym orężem, osiągając tym samym bonus do obrażeń. Wraz z postępami w trybie fabularnym stopniowo zdobywamy doświadczenie, a duże możliwości rozwoju postaci pozwalają dostosować umiejętności tak, by odpowiadały naszemu stylowi gry.</p>
				
				<p>Cechą charakterystyczną tytułu jest system poruszania się inspirowany dziełem studia EA DICE – Mirror’s Edge. Bohater potrafi skakać z budynku na budynek z gracją i zwinnością godną najlepszego parkourowca. Zastosowane przez twórców rozwiązanie stwarza nowe możliwości, a co za tym idzie – możemy zwiedzić praktycznie każdy zakątek miasta, niezależnie od tego, jak bardzo niedostępna wyda nam się droga do celu. Z kolei dynamiczny cykl dnia i nocy sprawia, że rozgrywka nie sprowadza się tylko do krwawej i bezlitosnej rzezi z mutantami. W Dying Light czasem lepiej uciec od przeważającej liczby przeciwników, niż stanąć z nimi twarzą w twarz, szczególnie w nocy, kiedy otoczenie gry zmienia się nie do poznania.</p>
				

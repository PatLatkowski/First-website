			<h1>E - Sport</h1>
			
			<p>Sport elektroniczny (e-sport, ang. electronic sports) – forma rywalizacji, w której przedmiotem działań zawodników są gry komputerowe. Rywalizacja między zawodnikami (graczami) odbywa się zarówno w formie rekreacyjnej, jak i na turniejach gier komputerowych (tzw. „pro gaming”). Historia e-sportu wiąże się ze wzrastającą popularnością rozgrywek sieciowych w grach takich jak StarCraft, Counter-Strike, Quake, Warcraft II: Tides of Darkness czy League of Legends. Popularność Quake'a przyczyniła się do powstania w 1997 roku w Stanach Zjednoczonych ligi zawodowych graczy Cyberathlete Professional League. Gracze e-sportowi uczestniczą w turniejach takich jak World Cyber Games (WCG) i Electronic Sports World Cup (ESWC).</p>

			<p>Mecze rozgrywane są na żywo, a publiczność może oglądać graczy siedzących przed komputerami, zaś ich posunięcia śledzić na wielkich telebimach. Rozgrywki w internecie są wykorzystywane częściej przy eliminacjach. Formuła sportu elektronicznego przyciąga coraz większe rzesze graczy w wieku od kilku do kilkudziesięciu lat, głównie dzięki niwelowaniu barier fizycznych, spotykanych w sporcie tradycyjnym, przy jednoczesnym zachowaniu poczucia rywalizacji sportowej.</p>

			<p>Najpopularniejszym e-sportowym wydarzeniem w historii były Mistrzostwa Świata Sezonu 3 w League of Legends. Największa historyczna pieniężna pula nagród została zgromadzona w turnieju The International 2017 w grze komputerowej Dota 2 i wynosiła 24 787 916 dolarów.</p>

			<p>Firma Newzoo oszacowała w 2016 roku, że w 2019 roku sportem elektronicznym będzie interesować się około 345 milionów osób.</p>
			
			<p>Szczególnie bliskie mojemu sercu są rozgrywki w Counter-Strike: Global Offensive, które po krótce postaram się przedstawić.</p>
			

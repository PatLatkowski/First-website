			<h2><a href="http://www.gry-online.pl/S016.asp?ID=17175" target="_blank">Counter-Strike: Global Offensive</a></h2>
			
			<p>Nowa odsłona klasycznej strzelanki sieciowej, która stała się podstawą współczesnego esportu. Twórcy produkcji starali się wprowadzić sporo usprawnień do rozgrywki i jednocześnie zachować podstawowe cechy pierwowzoru. Gra jest skierowana przede wszystkim do fanów multiplayera i zawiera wiele trybów zabawy. Gracze wcielają się w niej w terrorystów lub antyterrorystów i prowadzą zacięte pojedynki.</p>
			
			<p>Naprzeciw stają dwa zespoły - terroryści oraz próbujące ich powstrzymać siły specjalne. Za każdego wyeliminowanego przeciwnika nagradzani jesteśmy gotówką, za którą na początku kolejnej rundy możemy kupić lepszą broń i uzbrojenie.</p>
			
			<p>Counter-Strike znajduje się w ścisłej czołówce biorąc pod uwagę popularność w dziedzinie E-Sportu zarówno w oglądalności jak i liczbie graczy biorących udział w różnego rodzaju turniejach organizowanych na całym świecie. W Turniejach rangi Major, (największe i najbardziej prestiżowe) biorą udział głównie drużyny ze ścisłej czołówki CSowej sceny esportowej, a ich zmagania oglądają setki tysięcy osób. Majory odbywają się 2 do 3 razy w roku a ich pula nagród niejednokrotnie wynosiła nawet milion dolarów. W Polsce miały miejsce już 3 tego typu finały. W 2014 (Katowice) gdzie wygrała polska drużyna Virtus Pro (w składzie TaZ, Snax, Byali, NEO, pashaBiceps), w 2015 (również w Katowicach) gdzie Polacy odpadli w półfinałach oraz w 2017 w Krakowie. Organizowanie Majorów w Polsce podkreśla jak dużą popularnością CS cieszy się wśród Polaków. Ponadto każdego miesiąca organizowane są dziesiątki większych bądź mniejszych turniejów (Onlineowych lub LANowych) w których każdy z graczy CSGO może wziąć udział wraz ze swoją drużyną. </p>


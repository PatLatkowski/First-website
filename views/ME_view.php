				<h2><a href="http://www.gry-online.pl/gry-z-serii-i-podobne.asp?ID=1706" target="_blank">Mass Effect (Seria)</a></h2>
				
				<p>Mass Effect – stworzona przez kanadyjskie studio BioWare seria fantastycznonaukowych komputerowych gier fabularnych, wydana na platformy Microsoft Windows, Xbox 360 i PlayStation 3. Mass Effect łączy ze sobą elementy science-fiction, gier akcji, fabularnych i third-person shooterów. Początkowo została zaplanowana jako trylogia, jednak dyrektor generalny BioWare'u, David Silverman, zapewnił, że po wydaniu trzeciej części studio zamierza kontynuować serię.</p>

				<p>Akcja serii osadzona jest w przyszłości, w której ludzie dzięki odkryciu technologii protean rozprzestrzenili się po Drodze Mlecznej i poznali inne inteligentne rasy. Bohaterem trylogii jest komandor Shepard, który (lub która, ponieważ gra daje możliwość kierowania postacią męską bądź żeńską) zostaje pierwszym w historii ludzkim Widmem – elitarnym agentem Rady Cytadeli będącej galaktycznym rządem. Podczas wykonywania misji towarzyszy mu dwuosobowa drużyna postaci niezależnych kierowanych przez sztuczną inteligencję. Członków drużyny można rekrutować z różnych miejsc odwiedzonych przez głównego bohatera, w wyniku czego dołączają oni do załogi jego statku SSV Normandia. Głównym celem gracza jest uratowanie galaktyki przed mechaniczną rasą Żniwiarzy i ich popleczników, którzy chcą zniszczyć wszystkie wysoko rozwinięte cywilizacje.</p>

				<p>Seria osiągnęła duży sukces komercyjny i otrzymała bardzo dobre oceny recenzentów. Gry są chwalone za fabułę, postaci i romanse między nimi, wybory fabularne i głębię galaktyki.</p>
		
				<ul>
					<li>Mass Effect (2007) – gra pierwotnie ukazała się wyłącznie na konsoli Xbox 360, jednakże 28 maja 2008 roku doczekała się portu na platformę Microsoft Windows. 4 grudnia 2012 roku ukazała się na PlayStation 3 w usłudze PlayStation Network oraz jako część pakietu Mass Effect Trilogy. Shepard stara się pokrzyżować szyki Sarenowi Arteriusowi, chcącemu za pomocą armii gethów, mechanicznych form życia, przejąć władzę nad galaktyką. W trakcie śledztwa bohater odkrywa, że Saren został zindoktrynowany przez Suwerena – przedstawiciela potężnej rasy Żniwiarzy, którzy co pięćdziesiąt tysięcy lat unicestwiają wszystkie wysoko rozwinięte cywilizacje zamieszkujące Drogę Mleczną.</li>
					<li>Mass Effect 2 (2010) – część druga zadebiutowała na obu platformach 26 stycznia (Ameryka Północna) i 29 stycznia (Europa) 2010 roku. Istnieje możliwość zaimportowania do gry zapisów z poprzedniej części, dzięki czemu podjęte w niej decyzje mają wpływ na przebieg rozgrywki w Mass Effect 2. W fabule dużą rolę odgrywa założona przez ludzi organizacja Cerberus, w pierwszej części przedstawiana w złym świetle. Mass Effect 2 zebrała bardzo dobre recenzje, niektórzy krytycy wskazywali, że grę można wskazywać jako kandydata do tytułu gry roku. Recenzenci chwalili fabułę, postaci, grę aktorów głosowych, przemyślaną rozgrywkę oraz usprawnienia względem pierwszej części. Premiera gry na konsolę PlayStation 3 w USA odbyła się 18 stycznia 2011 roku, a w Europie 21 stycznia 2011 roku. Wydanie zawierało wszystkie wydane do czasu premiery DLC oraz interaktywny komiks, który streszczał wydarzenia poprzedniej części.</li>
					<li>Mass Effect 3 (2012) – premiera trzeciej części odbyła się 6 marca 2012 roku na platformy Microsoft Windows, PlayStation 3 i Xbox 360. Po raz pierwszy w serii oferuje grę wieloosobową w trybie kooperacji. W trzeciej części Shepard musi uratować Ziemię i całą galaktykę przed Żniwiarzami, które w końcu do niej dotarły. Casey Hudson, kierownik projektu w BioWare, stwierdził, że „sprawa z Mass Effect 3 jest łatwiejsza, ponieważ nie trzeba martwić się o planowanie kontynuacji”. Także w tej części można zaimportować zapis gry z poprzedniczki, co w momencie rozpoczęcia gry spersonalizuje rozgrywkę.</li>
					<li>Mass Effect: Andromeda – czwarta pełnoprawna odsłona serii, której premiera miała miejsce 21 marca 2017 roku. Gra jest oparta na silniku Frostbite 3, podobnie jak Dragon Age: Inkwizycja. Fabuła gry osadzona jest w galaktyce Andromedy, wiele lat po wydarzeniach z Mass Effect 3.</li>
				</ul>
	
